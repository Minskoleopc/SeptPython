
class Student:

    def setFullName(self, fn):
        self.fullName = fn
    def getFullName(self):
        return self.fullName

    def setAge(self, ag):
        self.age = ag
    def getAge(self):
        return self.age

    def setAdharNo(self, no):
        self.adharNo = no
    def getAdharNo(self):
        return self.adharNo

kajal = Student()
kajal.setFullName("kajal dule")
kajal.setAge(27)
kajal.setAdharNo(123)
print(kajal.fullName)
print(kajal.age)
print(kajal.adharNo)




